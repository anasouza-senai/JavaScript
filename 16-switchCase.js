var menuSelecionado = "Home"

switch (menuSelecionado) {
    case "Home":
        console.log(`Você clicou no link HOME`);
        break;
        case "Quem Somos":
            console.log(`Você clicou no link Quem Somos`);
        break;
    case "Contato":
        console.log(`Você clicou no link Contato`);
        break
    default:
          console.log(`Opção inválida`);
          break;
}









